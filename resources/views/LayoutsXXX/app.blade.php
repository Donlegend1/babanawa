// Function to fetch and update table data based on filters
function updateTable() {
    var selectedDate = $('#datepicker').val();
    var selectedExpenseType = $('#expenseType').val();
    var filterData = {};

    // Check which filter is selected and add it to the filterData object
    if (selectedDate) {
        filterData.date = selectedDate;
    }

    if (selectedExpenseType) {
        filterData.expense_type = selectedExpenseType;
    }

    // Make an AJAX request to the backend API endpoint
    $.ajax({
        url: '/expenses/filter', // Update with your backend API endpoint
        type: 'GET',
        data: filterData, // Send the filterData object containing selected filters
        success: function(response) {
            // Clear existing table rows
            $('#expenseTable tbody').empty();

            // Add filtered data to the table
            $.each(response, function(index, expense) {
                var formattedDate = moment(expense.created_at).format('Do MMMM YYYY');
                var row = '<tr>' +
                    '<td>' + (index + 1) + '</td>' +
                    '<td>' + expense.expense_type_name + '</td>' +
                    '<td>' + expense.description + '</td>' +
                    '<td>' + expense.unit_price + '</td>' +
                    '<td>' + expense.qty + '</td>' +
                    '<td>' + expense.amount + '</td>' +
                    '<td>' + formattedDate + '</td>' +
                    '<td>' +
                    // Add edit and delete buttons here, similar to your existing code
                    '</td>' +
                    '</tr>';

                // Append the row to the table
                $('#expenseTable tbody').append(row);
            });
        },
        error: function(error) {
            console.error('Error:', error);
        }
    });
}



// Attach event listeners to datepicker and expenseType dropdown
$('#datepicker, #expenseType').change(function() {
    // Call the updateTable function whenever date or expense type changes
    updateTable();
});

